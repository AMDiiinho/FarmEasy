    private void atualizarTabela() {
        PessoaService service = new PessoaServiceImpl();
        List<PessoaDTO> lista = service.listar();
        
        DefaultTableModel dfm = (DefaultTableModel) tabelaJTable.getModel();
        int linhas = dfm.getRowCount();
        for (int i = 0; i < linhas; i++) {
            dfm.removeRow(0);
        }
        
        for (PessoaDTO pessoaDTO : lista) {
            Object[] linha = new Object[3];
            linha[0] = pessoaDTO.getId();
            linha[1] = pessoaDTO.getNome();
            linha[2] = pessoaDTO.getCpf();
            
            dfm.addRow(linha);
        }
    }

