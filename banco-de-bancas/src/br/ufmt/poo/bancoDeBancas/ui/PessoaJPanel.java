    private void salvarJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarJButtonActionPerformed
        // TODO add your handling code here:
        String id = idJTextField.getText();
        String nome = nomeJTextField.getText();
        String cpf = cpfJFormattedTextField.getText();
        
        PessoaDTO dto = new PessoaDTO(id, nome, cpf);
        
        // enviando para a service
        PessoaService service = new PessoaServiceImpl();
        try {
            service.salvar(dto);
            JOptionPane.showMessageDialog(this, "Salvo com Sucesso!");
            cancelarJButtonActionPerformed(evt);
            atualizarTabela();
        } catch(Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }
    }//GEN-LAST:event_salvarJButtonActionPerformed

    private void removerJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerJButtonActionPerformed
        // TODO add your handling code here:
        int total = tabelaJTable.getSelectedRowCount();
        if(total <= 0){
            JOptionPane.showMessageDialog(tabelaJTable, "Por favor selecione as linhas a serem removidas!");
            return;
        }
        try {
            int[] linhas = tabelaJTable.getSelectedRows();
            PessoaService service = new PessoaServiceImpl();
            for (int linha : linhas) {
                String id = (String) tabelaJTable.getValueAt(linha, 0);
                PessoaDTO dto = new PessoaDTO(id, null, null);
                service.remover(dto);
            }
            JOptionPane.showMessageDialog(tabelaJTable, "Registros removidos com sucesso!");
            atualizarTabela();
        } catch (Exception erro) {
            erro.printStackTrace();
            JOptionPane.showMessageDialog(tabelaJTable, erro.getMessage());
        }
    }//GEN-LAST:event_removerJButtonActionPerformed

    private void editarJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarJButtonActionPerformed
        // TODO add your handling code here:
        int total = tabelaJTable.getSelectedRowCount();
        if(total != 1){
            JOptionPane.showMessageDialog(tabelaJTable, "Por favor selecione somente 1 linha para editar!");
            return;
        }
        int linha = tabelaJTable.getSelectedRow();
        String id = (String) tabelaJTable.getValueAt(linha, 0);
        String nome = (String) tabelaJTable.getValueAt(linha, 1);
        String cpf = (String) tabelaJTable.getValueAt(linha, 2);
        
        idJTextField.setText(id);
        nomeJTextField.setText(nome);
        cpfJFormattedTextField.setText(cpf);
    }//GEN-LAST:event_editarJButtonActionPerformed


